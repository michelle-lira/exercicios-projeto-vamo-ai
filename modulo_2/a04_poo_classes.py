# Crie uma classe Cachorro e seus métodos

class Cachorro:
    def __init__(self, nome, cor, raca, porte, quantidade):
        self.nome = nome
        self.cor = cor
        self.raca = raca
        self.porte = porte
        self.quantidade = quantidade
        

        # Métodos do Objeto Cachorro:
        def comer(self, local, racao):
            self.local = local
            self.racao = racao


        def dormir(self, local, horario):
            self.local = local
            self.horario = horario


        def latir(self, tipo_latido):
            self.tipo_latido = tipo_latido


        def coco(self, local_de_fazer):
            self.local_de_fazer = local_de_fazer


        def xixi(self, local_de_fazer):
            self.local_de_fazer = local_de_fazer


        def brincadeiras(self, tipo_de_brincadeira):
            self.tipo_de_brincadeira = tipo_de_brincadeira










